/**
 * Build script: generates src/lib/cloudWorker/deployScript.ts from
 * cloud-worker/src/ (the source of truth).
 *
 * Why: The Auto-Deploy flow (POST /api/settings/cloud-worker/deploy) needs
 * a single-file worker script to upload to Cloudflare's API. Previously,
 * deployScript.ts was a hand-maintained inline copy that drifted out of
 * sync with cloud-worker/src/. This script regenerates it automatically
 * by bundling the source with esbuild.
 *
 * Usage:
 *   node scripts/build/build-cloud-worker-deploy-script.mjs
 *
 * Output:
 *   src/lib/cloudWorker/deployScript.ts  (overwritten)
 *
 * The generated file exports `buildWorkerScript(kvNamespaceId, cloudSecret)`
 * which returns the final worker script string with the KV binding and
 * HMAC secret inlined.
 */

import { build } from "esbuild";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..", "..");
const SOURCE_ENTRY = resolve(ROOT, "cloud-worker/src/worker.ts");
const OUTPUT_FILE = resolve(ROOT, "src/lib/cloudWorker/deployScript.ts");

console.log("[build-cloud-worker] Bundling cloud-worker/src/worker.ts → deployScript.ts");

// Bundle the worker source into a single JS string.
// We use esbuild with format=esm, platform=browser, and bundle=true to
// inline all imports (routes/, lib/, types.ts) into one file.
const result = await build({
  entryPoints: [SOURCE_ENTRY],
  bundle: true,
  format: "esm",
  platform: "browser",
  target: "es2022",
  write: false,
  minify: false,
  sourcemap: false,
  legalComments: "none",
  define: {
    // Replace any process.env references with undefined — the worker
    // reads env from the `env` parameter, not process.env.
    "process.env.NODE_ENV": '"production"',
  },
});

const bundledCode = result.outputFiles[0].text;
console.log(`[build-cloud-worker] Bundled size: ${bundledCode.length} chars`);

// Wrap the bundled code in a function that takes kvNamespaceId + cloudSecret
// and returns the final worker script. The bundled code already exports
// `default` (the worker entry point), but Cloudflare needs a single file
// where `env.BUNDLES` and `env.CLOUD_SYNC_SECRET` are bound at runtime.
// The worker reads these from the `env` parameter, so we just need to
// make sure the bundled code doesn't reference process.env.
//
// The deploy route sets up the KV binding via the metadata.bindings field
// and the CLOUD_SYNC_SECRET via wrangler secrets, so the worker code itself
// doesn't need them inlined — it reads from `env`.

// Build the final TS file
const output = `/**
 * AUTO-GENERATED — DO NOT EDIT BY HAND.
 *
 * This file is regenerated by scripts/build/build-cloud-worker-deploy-script.mjs
 * from cloud-worker/src/ (the source of truth). Run that script after any
 * change to cloud-worker/src/.
 *
 * The Auto-Deploy flow (POST /api/settings/cloud-worker/deploy) uploads
 * this script to Cloudflare Workers via the API. The script is a single-file
 * ES module that:
 *   - Stores sync bundles in KV (POST /sync/:machineId)
 *   - Serves OpenAI-compatible endpoints (verify, models, chat/completions)
 *   - Signs non-streaming responses with HMAC-SHA256
 *   - Supports reasoningRoutingRules, api key ACL, provider fallback
 *
 * Placeholders (no longer needed — KV binding and secret are set via
 * Cloudflare API metadata, not inlined). The function signature is kept
 * for backward compatibility with the deploy route.
 */

export function buildWorkerScript(_kvNamespaceId: string, _cloudSecret: string): string {
  // KV binding and CLOUD_SYNC_SECRET are configured via Cloudflare API:
  //   - KV: metadata.bindings[{ type: "kv_namespace", name: "BUNDLES", ... }]
  //   - Secret: PUT /secrets with { name: "CLOUD_SYNC_SECRET", text: ... }
  // The worker reads them from the \`env\` parameter at runtime.
  // We keep the function signature for backward compatibility but no longer
  // inline these values.
  return ${JSON.stringify(bundledCode)};
}
`;

mkdirSync(dirname(OUTPUT_FILE), { recursive: true });
writeFileSync(OUTPUT_FILE, output, "utf-8");
console.log(`[build-cloud-worker] Wrote ${OUTPUT_FILE}`);
console.log(`[build-cloud-worker] Output size: ${output.length} chars`);
